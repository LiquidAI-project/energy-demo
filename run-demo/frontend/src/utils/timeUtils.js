// Convert the time to localTime when the unix time is given
export const convertToLocalTime = (unixTime) => {
    const date = new Date(unixTime * 1000);
    return date.toLocaleString('en-GB', {
      year: 'numeric',
      month: '2-digit',
      day: '2-digit',
      hour: '2-digit',
      minute: '2-digit',
      hour12: false,
    }).replace(/,\s/, ' '); // This will remove the comma between date and time
  };